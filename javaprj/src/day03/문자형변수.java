public class 문자형변수 {

	public static void main(String[] args) {
		
		// 모든 데이터는 숫자다
		// 'A' -> 65
		// 문자에 해당하는 숫자값이 있다. (아스키코드, utf-8)
		// 인코딩, 디코딩의 작업이 필요하다
		
		//'a' : 97
		//'A' : 65
		//'0' : 48
		
		// char : 2byte
		// 문자형변수 : 한 문자를 저장
		// 한문자 : 영문 한 개, 한글 한 개 '가', 'A', ' '
		
		char c = 'A'; // ' '사용
		System.out.println(c);
		
	}

}
