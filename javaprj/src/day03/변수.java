package day03;

public class 변수 {

	public static void main(String[] args) {
		// 변수 : 값을 저장하기 위한 메모리 공간(일정량의 바이트를 가진다)

		int a = 10; // 4byte, 21억
		double b = 253.23; // 8byte, 실수형 데이터를 저장할 수 있다
		boolean c = true; // 1byte, 참 / 거짓 데이터를 저장
		char d = 'J'; // 2byte ' ', 한문자 저장, 'a', '한'

		// 문자열은 문자 집합
		
		// 문자열
		String s = "hello java";
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
	}

}
